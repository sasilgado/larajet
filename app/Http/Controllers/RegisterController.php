     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $register = new Register();
        $register->documento = $request->get('documento');
        $register->tipoDocumento = $request->get('tipoDocumento');
        $register->nacimiento = $request->get('nacimiento');
        $register->genero = $request->get('genero');
        $register->nombres = $request->get('nombres');
        $register->apellidos = $request->get('apellidos');
        $register->color = $request->get('ubicacion');
        $register->save();

        return redirect('/register');
        //
    }

